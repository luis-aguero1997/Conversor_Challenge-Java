package Clases;

/**
 *
 * @author Luis
 */
public class Pesos {
    
    double Gramo, Kilogramo, Hectogramo, Decagramo, Decigramo, Centigramo, Miligramo;

    public Pesos() {
        this.Gramo = Gramo;
        this.Kilogramo = 0.001;
        this.Hectogramo = 0.001;
        this.Decagramo = 0.1;
        this.Decigramo = 10;
        this.Centigramo = 100;
        this.Miligramo = 1000;
    }

    public double getGramo() {
        return Gramo;
    }

    public void setGramo(double Gramo) {
        this.Gramo = Gramo;
    }

    public double getKilogramo() {
        return Kilogramo;
    }

    public void setKilogramo(double Kilogramo) {
        this.Kilogramo = Kilogramo;
    }

    public double getHectogramo() {
        return Hectogramo;
    }

    public void setHectogramo(double Hectogramo) {
        this.Hectogramo = Hectogramo;
    }

    public double getDecagramo() {
        return Decagramo;
    }

    public void setDecagramo(double Decagramo) {
        this.Decagramo = Decagramo;
    }

    public double getDecigramo() {
        return Decigramo;
    }

    public void setDecigramo(double Decigramo) {
        this.Decigramo = Decigramo;
    }

    public double getCentigramo() {
        return Centigramo;
    }

    public void setCentigramo(double Centigramo) {
        this.Centigramo = Centigramo;
    }

    public double getMiligramo() {
        return Miligramo;
    }

    public void setMiligramo(double Miligramo) {
        this.Miligramo = Miligramo;
    }
    
    
    
}
